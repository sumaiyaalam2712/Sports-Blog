                                together.</span>
                            <span class="post-devider">They are basketball brothers no more, with Thompson ending a
                                partnership that produced four championships with the Warriors and multitudes of
                                historic memories that enhanced the game.</span>
                            <span class="post-devider">He’s now in Dallas, joining the Mavericks in free agency, a
                                decision that served as an escape from a recent Warriors past that frustrated him. But
                                that decision also affected Curry, who finally had a chance to speak publicly Sunday
                                about what it all meant — and means.
                            </span>
                            <span class="post-devider">Curry took a break from Olympic preparations with Team USA to
                                address — make that mourn — the breakup of a generational backcourt. He chose a specific
                                word to describe the unwanted development.</span>
                            <span class="post-devider">
                                <span class="post-bold-font">
                                    “It (stinks).”
                                </span>
                            </span>
