                                jersey but more importantly the same enviable skill — shooting from distance — that
                                defined them as a duo. They’re the only teammates to combine for more than 600
                                3-pointers in a season (678 in 2015-16) and took turns competing with each other for
                                3-point feats. In 2018, Thompson made 14 from deep to beat Curry’s single-game record by
                                one.
                            </span>
                            <span class="post-devider">The sons of former NBA players, they were starting guards in an
                                All-Star Game (2015) and are linked as much, if not more, than any two teammates in NBA
                                history.</span>
                            <span class="post-devider">
                                But it went beyond that. Curry, Thompson and Draymond Green defined the Warriors’
                                championship dynasty, and Curry perhaps naively thought they’d be attached forever,
                                especially after Green signed an extension two summers ago.

                            </span>
                            <span class="post-devider">
                                <span class="post-bold-font">
                                    “It’s one of those hard things to kind of process just because I never imagined this
                                    would kind of be the reality,”
                                </span>
                                Curry said. “I always wanted to ride out in the sunset with those two guys and have an
                                opportunity to stay relevant from a winning perspective while we did it.”
                            </span>
