                                LeBron James.
                            </span>
                            <span class="post-devider">
                                “It’s kind of been a year-and-a-half long process where understanding where Klay was
                                with the decision that was in front of him, where the team was at in terms of the
                                extension offer,” Curry said.
                            </span>
                            <span class="post-devider">
                                “The negotiations that were going on, I knew most of the updates that were happening and
                                wanted to make sure Klay had the right support in terms of making the decision that was
                                best for him.”
                            </span>
                            <span class="post-devider">
                                Curry placed aside his selfish reasons — he wanted to keep Thompson — to realize those
                                reasons weren’t in Thompson’s best interest.
                            </span>
                            <span class="post-devider">
                                <span class="post-bold-font">
                                    “That’s what it came down to: he was in need of a change,”
                                </span>
