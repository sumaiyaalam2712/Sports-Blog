                                he meant to our organization. It was just one of those situations where you have to
                                trust he’s making the right decision for himself.
                                said Curry. “It wasn’t a situation where I needed to convince him to stay. I knew what
                                he meant to our organization. It was just one of those situations where you have to
                                trust he’s making the right decision for himself.

                            </span>
                            <span class="post-devider">
                                <span class="post-bold-font">
                                    “At the end of the day I want him to be happy.
                                </span>
                                He deserves to enjoy playing basketball however long he wants to play. It just (stinks)
                                that it won’t be with us.”
                            </span>
                            <span class="post-devider">
                                Curry obviously carries heavy clout within the organization, yet he admitted to being
                                told by Thompson not to flex it.
                            </span>
                            <span class="post-devider">
                                “Yeah,” Curry said. “Doesn’t mean I listened.”
                            </span>
                            <span class="post-devider">
                                In the end, it didn’t matter. Thompson’s mind was made up and his destination was
                                settled.
